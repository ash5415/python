## example of variable change

# Integer variable
age = 30
print(age)  # 30

# String variable
name = "Alice"
print(name)  # Alice

# Float variable
price = 19.99
print(price)  # 19.99

# Boolean variable
is_member = True
print(is_member)  # True

# List variable
colors = ["red", "green", "blue"]
print(colors)  # ['red', 'green', 'blue']

# Dictionary variable
person = {"name": "John", "age": 30}
print(person)  # {'name': 'John', 'age': 30}





